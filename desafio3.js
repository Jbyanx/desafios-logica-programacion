//contador de 1 a 10
let contador = 1;

while(contador <= 10 ){
    console.log(contador+"\n");
    contador++;
}

//contador de 10 a 0
let contador2 = 10;

while(contador2 >= 0 ){
    console.log(contador2+"\n");
    contador2--;
}

//cuenta regresiva
let numero = parseInt(prompt("Ingrese un numero"));

while(numero >= 0){
    console.log(numero+"\n");
    numero--;
}

//cuenta progresiva
let numero2 = parseInt(prompt("Ingrese un numero"));

while(numero2 <= 10){
    console.log(numero2+"\n");
    numero2++;
}
